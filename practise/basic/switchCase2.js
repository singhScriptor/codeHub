function switchCase(x){
    switch(x){
        case 1:
            console.log('Choice is 1')
            break
        case 2:
            console.log('choice is 2')
        case 3:
            console.log('choice is 3')
        case 4:
            console.log('choice is 4')
            break
        default:
            console.log('Choice other than 1,2,3 and 4')
            break    
    }
}
switchCase(2)