// While loop

function printSeries(n){
    let i=1
    while(i <= n){
        if(i*i <= n){
            console.log(i*i)
        }
        i++
    }
}
printSeries(49)



// for loop

function ShowSeries(num){
    for(let i=1;i<=num;i++){
        if(i*i <= num){
            console.log(i*i)
        }
    }
}
ShowSeries(49)